function showDetail(id) {
  currentPlace = PLACES.find(p => p.id === id);
  if (!currentPlace) return;
  document.getElementById('detailName').textContent = currentPlace.name;
  document.getElementById('detailDesc').textContent = currentPlace.description;
  document.getElementById('detailRating').innerHTML = `★ ${currentPlace.ratingAvg || '—'}`;
  document.getElementById('detailHours').textContent = currentPlace.openingHours || '';
  document.getElementById('detailImg').style.backgroundImage = currentPlace.imageUrl ? `url('${API_BASE.replace('/api', '') + currentPlace.imageUrl}')` : 'none';
  document.getElementById('detailCat').textContent = CATEGORY_LABELS[currentPlace.category];
  document.getElementById('detailPanel').classList.add('show');
  map.flyTo([currentPlace.latitude, currentPlace.longitude]);
}

