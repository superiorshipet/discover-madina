function renderPlaces() {
  const list = document.getElementById('placesList');
  list.innerHTML = PLACES.map(p => `
    <div class="place-item" onclick="showDetail(${p.id})">
      <div class="place-icon">${p.icon || '📍'}</div>
      <div>
        <div class="place-name">${p.name}</div>
        <div class="place-cat">${CATEGORY_LABELS[p.category] || p.category}</div>
      </div>
    </div>
  `).join('');
}

function addMarkers() {
  markers.forEach(m => map.removeLayer(m));
  markers = PLACES.map(p => L.marker([p.latitude, p.longitude], {
    icon: L.divIcon({ className: 'custom-marker', html: p.icon || '📍', iconSize: [30,30] })
  }).on('click', () => showDetail(p.id)).addTo(map));
}

