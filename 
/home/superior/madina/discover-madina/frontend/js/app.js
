// Chat
function sendSuggestion(btn) {
  const msg = btn.textContent;
  addChatMsg('user', msg);
  setTimeout(() => addChatMsg('bot', CHATBOT_RESPONSES[msg] || CHATBOT_RESPONSES.default), 800);
}
function addChatMsg(type, text) {
  const body = document.getElementById('chatbotBody');
  const div = document.createElement('div');
  div.className = `cb-msg ${type}`;
  div.innerHTML = `<div class="cb-bubble">${text}</div>`;
  body.appendChild(div);
  body.scrollTop = body.scrollHeight;
}
function sendChat() {
  const input = document.getElementById('chatInput');
  const msg = input.value.trim();
  if (!msg) return;
  input.value = '';
  addChatMsg('user', msg);
  setTimeout(() => addChatMsg('bot', CHATBOT_RESPONSES.default), 800);
}


